"use strict";
/**
 * Функция сложения двух параметров
 * @param {number} a
 * @param {number} b
 * @returns {number}
 */
function sum(a, b) {
  return a + b;
}

/**
 * Функция умножения двух параметров
 * @param {number} a
 * @param {number} b
 * @returns {number}
 */
function mul(a, b) {
  return a * b;
}

/**
 * Функция деления параметров
 * @param {number} a
 * @param {number} b
 * @returns {number}
 */
function div(a, b) {
  return a / b;
}

/**
 * Функция вычитания двух параметров
 * @param {number} a
 * @param {number} b
 * @returns {number}
 */
function ded(a, b) {
  return a - b;
}
